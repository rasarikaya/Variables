package Day03_Variables;

public class PrintStatementsPractise {

    public static void main(String[] args) {
        System.out.println("Hello"); // does not append new line
        System.out.println("Hola"); // does not append new line

        System.out.println("How are you?"); // appends new line after printing

        System.out.println("Today is Friday"); // does not append new line
        System.out.println("Tomorrow is Saturday");

        System.out.println(".....................");
        System.out.println("\tHello Batch 23\n\tHow are you today");
        System.out.println("My name is \"Muhtar\"");
        System.out.println("My name is 'name'");
    }
}
